 public void list() { //method to list airline points data and passengers
     String temp = ""; 
    
    /**The get(x) portion of the code goes to the “x” index of the ArrayList,
      * then looks for the value in the field defined by the object class. **/ 
     for (int x=0; x<=points.size()-1;x++) {
         temp = temp + points.get(x).firstName + " "
                 + points.get(x).lastName + " "
                 + points.get(x).week1 + " "
                 + points.get(x).week2 + " "
                 + points.get(x).week3 + " "
                 + points.get(x).week4 + "\n";
     }
     pointsOutput.setText(temp); //outputs the list of passenger points in the arraylist
         
 }
         
