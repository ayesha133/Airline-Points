   public void add() { //method to add user input to array
       
       airlinePoints c; // variable “c” sends the input information to the class.  
       String firstName, lastName, week1, week2, week3, week4;  
       int num1, num2, num3, num4; 
   
     /**Sorts user inputs to their appropriate variables **/   
     firstName = firstNameinput.getText(); 
     lastName = lastNameinput.getText(); 
    
  try { //try..catch to prevent the user from entering letters for points input
 
  /**Points input changed to integers */    
    num1 = Integer.parseInt(week1Input.getText());
    num2 = Integer.parseInt(week2Input.getText());
    num3 = Integer.parseInt(week3Input.getText());
    num4 = Integer.parseInt(week4Input.getText());
 
 /**If structure runs when points input does not equal a number. The opposite of what we want is coded.(It works that way).
  If structure also only runs when points are greater than or equal to 0 */  
    if ((!week1Input.getText().equals(num1)|| week2Input.getText().equals(num2) || week3Input.getText().equals(num3) || week4Input.getText().equals(num4))&& (num1 >=0 && num2>=0 && num3>=0&& num4>=0)) {    
  
   /**Sorts user inputs to their appropriate variables **/
     week1 = week1Input.getText(); 
     week2 = week2Input.getText(); 
     week3 = week3Input.getText();  
     week4 = week4Input.getText(); 
     c = new airlinePoints(firstName, lastName, week1, week2, week3, week4);
       points.add(c); //class is added to the points ArrayList
    }
    
  } catch (Exception e) {
      pointsOutput.setText("Numbers must be used for points"); // message is displayed when points have letters
     
      
  }
   }
   
       
    
