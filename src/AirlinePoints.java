  public void totalPoints() { //method for total points and bonus
    int total = 0; 
    int bonus = 0; 
   
    for (int i=0; i<=points.size()-1;i++) { 
        
     //if structure runs when the user's input for firstname and lastname match the data in the arraylist
     if ((firstNameinput.getText().equals(points.get(i).firstName)) && (lastNameinput.getText().equals(points.get(i).lastName))) { 
 
       /**Points are converted to integers */    
         int point1 = Integer.parseInt(points.get(i).week1);
         int point2 = Integer.parseInt(points.get(i).week2);
         int point3 = Integer.parseInt(points.get(i).week3);
         int point4 = Integer.parseInt(points.get(i).week4);
        total = point1 + point2 + point3 + point4; //points are added
        
        if (total > 5000) { //if total is greater than 5000 points, than passenger gets bonus 1000 points
            bonus = 1000; 
            
       /**These messages are displayed if user is qualified or not*/      
          JOptionPane.showMessageDialog(null,points.get(i).firstName + " you have qualified for 1000 bonus points!");
        } else
            JOptionPane.showMessageDialog(null,points.get(i).firstName + " you have not qualified for 1000 bonus points");
          
     } 
    } 
      pointsOutput.setText("Total is: " + total + " Bonus: " + bonus); //message displayed
            
  }
       
